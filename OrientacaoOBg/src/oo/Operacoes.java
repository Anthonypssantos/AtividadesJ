package oo;

public class Operacoes {

	public int soma(int a, int b) {
		int soma=0;
		
		soma = a + b;
		
		return soma;
	}
	
	public int subtrair(int a, int b) {
		int subtrair=0;
		
		subtrair = b - a;
		
		return subtrair;
	}
	
	public int dividir(int a, int b) {
		int dividir=0;
		
		dividir = a / b;
		
		return dividir;
	}
	
	public int multiplicar(int a, int b) {
		int multiplicar=0;
		
		multiplicar = a * b;
		
		return multiplicar;
	}
	
}
